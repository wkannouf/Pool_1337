#include <unistd.h>

int	main(int argc, char **argv)
{
	int	b;
	int	i;

	b = argc -1;
	while (b > 0)
	{
		i = 0;
		while (argv[b][i])
		{
			write(1, &argv[b][i], 1);
			i++;
		}
		write(1, "\n", 1);
		b--;
	}
}
